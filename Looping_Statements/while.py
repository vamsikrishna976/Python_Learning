
                                                 # While loop


# The while loop is used to execute a block of code repeatedly as long as a given condition is true.
# The while loop is used when the number of iterations is not known beforehand.
# Syntax:
# while condition:
#     statement
#
# Example:

i=1
while i<10:
    print(i)
    i=i+1

#output
# 1
# 2
# 3 
# 4
# 5
# 6
# 7
# 8
# 9
