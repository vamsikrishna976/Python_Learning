
                                                               # List operators in Python
# List operators that can be used with lists in Python
# Operators that can be used with lists in Python
# +, *, in, for, len
#concatination, repetition, membership, iteration, length

#concatination
# + operator concatenates two lists
l1 = [1,2,3]
l2 = [4,5,6]
print(l1+l2)

#repetition
# * operator repeats the list a specified number of times
l1 = [1, 2, 3]
print(l1*5)

# membership
# in operator checks if an element is present in the list
l1 = [1, 2, 3, 4, 5]
print(2 in l1)

# iteration
# for operator iterates over the elements of the list
l1 = [1, 2, 3, 4, 5]
for i in l1 :
    print(i)

# length
# len operator returns the number of elements in the list
l1 = [1, 2, 3, 4, 5]
print(len(l1))