
                                                                  #List Built-in Functions

# Built-in functions that can be used with lists in Python
# max, sum, min, list

# max
# The max() function returns the largest element in the list

l1=[1,2,3,4,5]
print(max(l1))

# sum
# The sum() function returns the sum of all elements in the list
l1 = [1, 2, 3, 4, 5]
print(sum(l1))

# min
# The min() function returns the smallest element in the list
l1 = [1, 2, 3, 4, 5]
print(min(l1))

# list
# The list() function creates a new list from an iterable (like a string or tuple)
l2 = "Vamsi"
print(list(l2))